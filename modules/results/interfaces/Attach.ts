export interface Attach {
  id: number
  name: string
  file: string
  patient: Patient
  status: number
  diary: Diary
  service: Service
  codeAutogenerated: string
}

export interface Patient {
  id: number
  document: string
  name: string
  lastname: string
  age: number
  landline: string
  mobilePhone: string
}

export interface Diary {
  id: number
  includeHoliday: boolean
  duplicate: boolean | null
  dates: string[]
  initialHour: string
  finalHour: string
}

export interface Service {
  id: number
  code: string
  description: string
  valueProgram: number | null
}

export interface FileProcessRequest {
  patientId: number
  fileLink: string
  code: string
}
